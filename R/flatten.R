LeafNode <- function(i) {
  structure(list(i = i), class = c("LeafNode", "Node"))
}

ListNode <- function(nodes, names) {
  structure(
    list(
      nodes = unname(nodes),
      names = names
    ),
    class = c("ListNode", "Node")
  )
}

MarkedListNode <- function(nodes, names, marked) {
  structure(
    list(
      nodes = unname(nodes),
      names = names,
      marked = marked
    ),
    class = c("MarkedListNode", "ListNode")
  )
}
